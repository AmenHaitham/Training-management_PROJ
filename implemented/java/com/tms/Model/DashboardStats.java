package com.tms.Model;

public class DashboardStats {
    private int upcomingSessions;
    private int activeCourses;
    private double attendanceRate;
    private int certificatesCount;
    public int getUpcomingSessions() {
        return upcomingSessions;
    }
    public void setUpcomingSessions(int upcomingSessions) {
        this.upcomingSessions = upcomingSessions;
    }
    public int getActiveCourses() {
        return activeCourses;
    }
    public void setActiveCourses(int activeCourses) {
        this.activeCourses = activeCourses;
    }
    public double getAttendanceRate() {
        return attendanceRate;
    }
    public void setAttendanceRate(double attendanceRate) {
        this.attendanceRate = attendanceRate;
    }
    public int getCertificatesCount() {
        return certificatesCount;
    }
    public void setCertificatesCount(int certificatesCount) {
        this.certificatesCount = certificatesCount;
    }
 
    // getters and setters
    
}
