package com.tms.Model;

public class Dashboard {
    
    private int totalTrainees;
    private int totalTrainers;
    private int totalTrainings;
    private int totalCourses;
    private int totalSessions;
    private int completedSessions;
    private int canceledSessions;
    private int feedbackRecived;
    
    public int getTotalTrainees() {
        return totalTrainees;
    }
    public void setTotalTrainees(int totalTrainees) {
        this.totalTrainees = totalTrainees;
    }
    public int getTotalTrainers() {
        return totalTrainers;
    }
    public void setTotalTrainers(int totalTrainers) {
        this.totalTrainers = totalTrainers;
    }
    public int getTotalTrainings() {
        return totalTrainings;
    }
    public void setTotalTrainings(int totalTrainings) {
        this.totalTrainings = totalTrainings;
    }
    public int getTotalCourses() {
        return totalCourses;
    }
    public void setTotalCourses(int totalCourses) {
        this.totalCourses = totalCourses;
    }
    public int getTotalSessions() {
        return totalSessions;
    }
    public void setTotalSessions(int totalSessions) {
        this.totalSessions = totalSessions;
    }
    public int getCompletedSessions() {
        return completedSessions;
    }
    public void setCompletedSessions(int completedSessions) {
        this.completedSessions = completedSessions;
    }
    public int getCanceledSessions() {
        return canceledSessions;
    }
    public void setCanceledSessions(int canceledSessions) {
        this.canceledSessions = canceledSessions;
    }
    public int getFeedbackRecived() {
        return feedbackRecived;
    }
    public void setFeedbackRecived(int feedbackRecived) {
        this.feedbackRecived = feedbackRecived;
    }

    
}
